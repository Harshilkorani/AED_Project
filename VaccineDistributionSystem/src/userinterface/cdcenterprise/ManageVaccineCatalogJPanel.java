    private void refreshTable()
    {
        int rowCount = vaccineCatalog.getRowCount();
        DefaultTableModel model = (DefaultTableModel)vaccineCatalog.getModel();
        for(int i=rowCount-1;i>=0;i--) {
            model.removeRow(i);
        }
        
        for(Vaccine vacc : cdcEnterprise.getApprovedVaccineCatalog().getVaccineList())
        {
            Object row[] = new Object[3];
            row[0] = vacc;
            row[1] = vacc.getVaccineID();
            row[2] = vacc.getVaccinePrice();
            model.addRow(row);
        }
    }
    
    
