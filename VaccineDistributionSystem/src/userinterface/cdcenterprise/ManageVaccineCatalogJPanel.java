    private void btnDeleteVaccineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteVaccineActionPerformed
        // TODO add your handling code here:
        int row = vaccineCatalog.getSelectedRow();
        if(row >=0) 
        {
            Vaccine v = (Vaccine)vaccineCatalog.getValueAt(row, 0);
            
            int selectedOption = JOptionPane.showConfirmDialog(null,"Are you sure you want to delete","Warning", JOptionPane.YES_NO_OPTION);
            if(selectedOption == JOptionPane.YES_NO_OPTION)
            {
                cdcEnterprise.getApprovedVaccineCatalog().removeVaccine(v);
                //refresh table
                refreshTable();
                
            }
            return;
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Please select a ROW", "ERROR", JOptionPane.INFORMATION_MESSAGE);
        }
     
    }//GEN-LAST:event_btnDeleteVaccineActionPerformed

